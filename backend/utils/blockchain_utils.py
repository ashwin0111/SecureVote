"""
Blockchain Utility — SecureVote
Connects to Ganache local node via Web3.py.
Zero gas cost — uses Ganache's pre-funded accounts.

Smart Contract: VotingContract.sol (compiled ABI embedded below)
Handles:
  - castVote(token, candidateId)          → records vote on-chain
  - hasVoted(token)                       → checks duplicate
  - getVoteCount(candidateId)             → tally
  - isElectionActive()                    → gate check
"""

import os
