def generate_anonymous_token(voter_uuid: str, election_id: str) -> str:
    """
    Generate one-time anonymous voting token.
    Uses HMAC so it's unpredictable but deterministic per voter+election.
    Once used, the smart contract nullifies it.
    """
    secret = current_app.config.get("JWT_SECRET", "jwt-secret")
    random_salt = secrets.token_hex(16)
    raw = f"{voter_uuid}:{election_id}:{random_salt}:{secret}"
    token_hash = hashlib.sha256(raw.encode()).hexdigest()
    return f"SVT-{token_hash[:40].upper()}"


def get_blockchain_explorer_url(tx_hash: str) -> str:
    """Returns Ganache explorer URL (local)."""
    return f"http://localhost:7545/tx/{tx_hash}"
